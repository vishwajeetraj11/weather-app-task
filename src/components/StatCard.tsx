type Props = {
  title: string;
  stat: number;
  unit: string;
  type: "visibility" | "humidity" | "wind";
};

const StatCard = (props: Props) => {
  const { title, stat, unit, type } = props;
  const isVisibility = type === "visibility";
  const isHumidity = type === "humidity";
  const isWind = type === "wind";
  const Icon = isWind
    ? WindIcon
    : isHumidity
    ? HumidIcon
    : isVisibility
    ? Eye
    : null;

  return (
    <div className="flex items-start flex-col gap-2 shadow-light px-3 py-2 rounded-sm">
      {Icon && <Icon />}
      <p>
        {stat} {unit}
      </p>
      <p className="text-xs">{title}</p>
    </div>
  );
};

export default StatCard;

